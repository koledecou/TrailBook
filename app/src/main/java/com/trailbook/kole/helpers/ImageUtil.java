    public static int getOrientationForGalleryImage(Context context, Uri photoUri) {
        Cursor cursor = context.getContentResolver().query(photoUri,
                new String[] { MediaStore.Images.ImageColumns.ORIENTATION },
                null, null, null);

        try {
            if (cursor.moveToFirst()) {
                return cursor.getInt(0);
            } else {
                return -1;
            }
        } finally {
            cursor.close();
        }
    }

    public static Bitmap scaleBitmapToWidth(Bitmap bitmap, int newWidth) {
        int width = bitmap.getWidth();
        int height = bitmap.getHeight();

        double ratio = (double)height/(double)width;
        int newHeight = (int)Math.round(newWidth*ratio);

        return Bitmap.createScaledBitmap(bitmap, newWidth, newHeight, false);
    }

    public static Bitmap getRotatedBitmapFromGallery(Context context, Uri photoUri, int targetSize) throws IOException {
        Bitmap bitmap = MediaStore.Images.Media.getBitmap(context.getContentResolver(),photoUri);
        bitmap = scaleBitmapToWidth(bitmap, targetSize);
        int orientation = getExifOrientation(getOrientationForGalleryImage(context, photoUri));
        return rotateBitmap(bitmap, orientation);
    }

